#include<stdio.h>
int main() {
    //把自己的想法实现的过程就是创造，
    //把自己的问题回答的过程就是创造，
    //把自己的猜测验证的过程就是创造，
    //所以做好以上工作，你就是在创造！
//1 1 1 1->3,3,3,3->3-----3
//1 1 2 2->1,1,1,1->2-----1
//1 1 1 2->2,2,2,0->2-----2
//1 1 2 1->2,2,0,2->2-----2
//1 1 2 3->1,1,0,0->1-----1
//1 2 1 1->2,0,2,2->2-----2
//1 2 2 2->0,2,2,2->2-----2
//1 2 3 3->0,0,1,1->1-----1
//1 2 1 2->1,1,1,1->2-----1
//1 2 1 3->1,0,1,0->1-----1
//1 2 3 2->0,1,0,1->1-----1
//1 2 2 1->1,1,1,1->2-----1
//1 2 3 1->1,0,0,1->1-----1
//1 2 2 3->0,1,1,0->1-----1
//1 2 3 4->0,0,0,0->0-----0
//
//采用最大值方法，不一致的地方是！
//1 1 2 2->1,1,1,1->2-----1
//1 2 1 2->1,1,1,1->2-----1
//1 2 2 1->1,1,1,1->2-----1
//
//1 1 2 3->1,1,0,0->1-----1
//1 2 3 3->0,0,1,1->1-----1
//1 2 1 3->1,0,1,0->1-----1
//1 2 3 2->0,1,0,1->1-----1
//1 2 3 1->1,0,0,1->1-----1
//1 2 2 3->0,1,1,0->1-----1

    int s[4];
    int i,j;
    for(i=0; i<4; i++) {
        scanf("%d",s+i);
    }
    int max_diff=0;
    int have_zero=0;
    for(i=0; i<4; i++) {
        int diff=0;
        for(j=0; j<4; j++) {
            if(i!=j) {
                if(s[i]==s[j]) diff++;
            }
        }
        if(diff==0) {
            have_zero=1;
        }
        if(diff>max_diff) {
            max_diff=diff;
        }
    }
    if(max_diff==1 && !have_zero) {
        printf("2\n");
    } else {
        printf("%d\n",max_diff);
    }
    return 0;
}
